class NoMatchingTool(Exception):
    def __init__(self, message):
        self.message = message
        super().__init__(self.message)

@dataclass
class ToolResult:
    """
    Represents the result of a tool invocation within the ToolBox framework.

    Attributes:
        tool_call_id (str): A unique identifier for the tool call.
        name (str): The name of the tool that was called.
        output (Optional[Any]): The output generated by the tool call, if any.
        arguments (Optional[dict[str, Any]]): The arguments passed to the tool call.
        error (Optional[Exception]): An error message if the tool call failed.
        stack_trace (Optional[str]): The stack trace if the tool call failed.
        soft_errors (list[Exception]): A list of non-critical error messages encountered during the tool call.
        tool (Optional[Union[Callable, BaseModel]]): The function or model that was called.

    Methods:
        to_message(): Converts the ToolResult into a dictionary suitable for returning to a chat interface.
    """
    tool_call_id: str
    name: str
    output: Optional[Any] = None
    arguments: Optional[dict[str, Any]] = None
    error: Optional[Exception] = None
    stack_trace: Optional[str] = None
    soft_errors: list[Exception] = field(default_factory=list)
    tool: Optional[Union[Callable, BaseModel]] = None

    def to_message(self) -> dict[str, str]:
        """
        Converts the ToolResult into a dictionary suitable for returning to a chat interface.

        Returns:
            dict[str, str]: A dictionary containing the role, tool_call_id, name, and content of the tool result.
        """
        if self.error is not None:
            content = f"{self.error}"
        elif self.output is None:
            content = ''
        elif isinstance(self.output, BaseModel):
            content = f"{self.name} created"
        else:
            content = str(self.output)
        return {
            "role": "tool",
            "tool_call_id": self.tool_call_id,
            "name": self.name,
            "content": content,
        }

def process_tool_call(tool_call, functions_or_models, fix_json_args=True, case_insensitive=False) -> ToolResult:
    """
    Processes a single tool call by matching it to a function or model and executing it.

    Args:
        tool_call (ChatCompletionMessageToolCall): The tool call to process.
        functions_or_models (list[Union[Callable, BaseModel]]): A list of functions or models to match against.
        fix_json_args (bool): Whether to attempt to fix JSON decoding errors in the tool call arguments.
        case_insensitive (bool): Whether to perform case-insensitive matching of tool names.

    Returns:
        ToolResult: The result of the tool call.
    """
    function_call = tool_call.function
    tool_name = function_call.name
    args = function_call.arguments
    soft_errors: list[Exception] = []
    error = None
    stack_trace = None
    output = None
    try:
        tool_args = json.loads(args)
    except json.decoder.JSONDecodeError as e:
        if fix_json_args:
            soft_errors.append(e)
            args = args.replace(', }', '}').replace(',}', '}')
            tool_args = json.loads(args)
        else:
            stack_trace = traceback.format_exc()
            return ToolResult(tool_call_id=tool_call.id, name=tool_name, error=e, stack_trace=stack_trace)

    tool = None

    for f in functions_or_models:
        if get_name(f, case_insensitive=case_insensitive) == tool_name:
            tool = f
            try:
                output, new_soft_errors = _process_unpacked(f, tool_args, fix_json_args=fix_json_args)
                soft_errors.extend(new_soft_errors)
            except Exception as e:
                error = e
                stack_trace = traceback.format_exc()
            break
    else:
        error = NoMatchingTool(f"Function {tool_name} not found")
    return ToolResult(
        tool_call_id=tool_call.id, 
        name=tool_name,
        arguments=tool_args,
        output=output, 
        error=error,
        stack_trace=stack_trace,
        soft_errors=soft_errors,
        tool=tool,
    )

def split_string_to_list(s: str) -> list[str]:
    """
    Splits a string into a list of strings, handling potential JSON encoding issues.

    Args:
        s (str): The string to split.

    Returns:
        list[str]: A list of strings.
    """
    try:
        return json.loads(s)
    except json.JSONDecodeError:
        return [item.strip() for item in s.split(',')]

def _process_unpacked(function, tool_args={}, fix_json_args=True):
    """
    Processes the tool arguments and calls the function with the unpacked arguments.

    Args:
        function (Union[Callable, LLMFunction]): The function to call.
        tool_args (dict[str, Any]): The arguments to pass to the function.
        fix_json_args (bool): Whether to attempt to fix JSON decoding errors in the tool call arguments.

    Returns:
        tuple[Any, list[Exception]]: The result of the function call and a list of soft errors.
    """
    if isinstance(function, LLMFunction):
        function = function.func
    model = parameters_basemodel_from_function(function)
    soft_errors = []
    if fix_json_args:
        for field, field_info in model.model_fields.items():
            field_annotation = field_info.annotation
            if _is_list_type(field_annotation):
                if field in tool_args and isinstance(tool_args[field], str):
                    tool_args[field] = split_string_to_list(tool_args[field])
                    soft_errors.append(f"Fixed JSON decode error for field {field}")

    model_instance = model(**tool_args)
    args = {}
    for field, _ in model.model_fields.items():
        args[field] = getattr(model_instance, field)
    return function(**args), soft_errors

def _is_list_type(annotation):
    """
    Checks if the given annotation is a list type.

    Args:
        annotation (Any): The type annotation to check.

    Returns:
        bool: True if the annotation is a list type, False otherwise.
    """
    origin = get_origin(annotation)
    args = get_args(annotation)

    if origin is list:
        return True
    elif origin is Union or origin is Optional:
        return any(_is_list_type(arg) for arg in args)
    return False

def process_response(response: ChatCompletion, functions: list[Union[Callable, LLMFunction]], choice_num=0, **kwargs) -> list[ToolResult]:
    """
    Processes a ChatCompletion response, executing contained tool calls.

    Args:
        response (ChatCompletion): The response object containing tool calls.
        functions (list[Union[Callable, LLMFunction]]): A list of functions or models to call.
        choice_num (int, optional): The index of the choice to process from the response. Defaults to 0.

    Returns:
        list[ToolResult]: A list of ToolResult objects, each representing the outcome of a processed tool call.
    """
    message = response.choices[choice_num].message
    return process_message(message, functions, **kwargs)

def process_message(
    message: ChatCompletionMessage,
    functions: list[Union[Callable, LLMFunction]],
    fix_json_args=True,
    case_insensitive=False,
    executor: Optional[Union[ThreadPoolExecutor, ProcessPoolExecutor]]=None
    ) -> list[ToolResult]:
    """
    Processes a message containing tool calls.

    Args:
        message (ChatCompletionMessage): The message containing tool calls.
        functions (list[Union[Callable, LLMFunction]]): A list of functions or models to call.
        fix_json_args (bool): Whether to attempt to fix JSON decoding errors in the tool call arguments.
        case_insensitive (bool): Whether to perform case-insensitive matching of tool names.
        executor (Optional[Union[ThreadPoolExecutor, ProcessPoolExecutor]]): An executor to use for parallel processing.

    Returns:
        list[ToolResult]: A list of ToolResult objects, each representing the outcome of a processed tool call.
    """
    results = []
    if hasattr(message, 'function_call') and (function_call:=message.function_call):
        tool_calls = [ChatCompletionMessageToolCall(id='A', function=Function(name=function_call.name, arguments=function_call.arguments), type='function')]
    elif hasattr(message, 'tool_calls') and message.tool_calls:
        tool_calls = message.tool_calls
    else:
        tool_calls = []

    if not tool_calls:
        return []
    args_list = [(tool_call, functions, fix_json_args, case_insensitive) for tool_call in tool_calls]

    if executor:
        results = list(executor.map(lambda args: process_tool_call(*args), args_list))
    else:
        results = list(map(lambda args: process_tool_call(*args), args_list)) 
    return results

def process_one_tool_call(
        response: ChatCompletion,
        functions: list[Union[Callable, LLMFunction]],
        index: int = 0,
        fix_json_args=True,
        case_insensitive=False
    ) -> Optional[ToolResult]:
    """
    Processes a single tool call from a ChatCompletion response at the specified index.

    Args:
        response (ChatCompletion): The response object containing tool calls.
        functions (list[Union[Callable, LLMFunction]]): A list of functions or models to call.
        index (int, optional): The index of the tool call to process. Defaults to 0.
        fix_json_args (bool): Whether to attempt to fix JSON decoding errors in the tool call arguments.
        case_insensitive (bool): Whether to perform case-insensitive matching of tool names.

    Returns:
        Optional[ToolResult]: The result of the processed tool call, or None if the index is out of range.
    """
    tool_calls = _get_tool_calls(response)
    if not tool_calls or index >= len(tool_calls):
        return None

    return process_tool_call(tool_calls[index], functions, fix_json_args, case_insensitive)

def _get_tool_calls(response: ChatCompletion) -> list[ChatCompletionMessageToolCall]:
    """
    Extracts tool calls from a ChatCompletion response.

    Args:
        response (ChatCompletion): The response object containing tool calls.

    Returns:
        list[ChatCompletionMessageToolCall]: A list of tool calls.
    """
    if hasattr(response.choices[0].message, 'function_call') and (function_call := response.choices[0].message.function_call):
        return [ChatCompletionMessageToolCall(id='A', function=Function(name=function_call.name, arguments=function_call.arguments), type='function')]
    elif hasattr(response.choices[0].message, 'tool_calls') and response.choices[0].message.tool_calls:
        return response.choices[0].message.tool_calls
    return []

